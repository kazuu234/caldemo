// 国から地域を取得
export const getRegionByCountry = (country: string): Region | undefined => {
  for (const [region, countries] of Object.entries(COUNTRIES_BY_REGION)) {
    if (countries.some(c => c.name === country)) {
      return region as Region;
    }
  }
  return undefined;
};

// 地域から国リストを取得
export const getCountriesByRegion = (region: Region): Array<{name: string, emoji: string}> => {
  return COUNTRIES_BY_REGION[region] || [];
};
